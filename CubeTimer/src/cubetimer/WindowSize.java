package cubetimer;

public class WindowSize{
	
	public int windowWidth;
	public int windowHeight;
	
	public WindowSize(){
	
		windowWidth = 0;
		windowHeight = 0;
	}
	
	public void setSize(int windowWidthIn, int windowHeightIn){
		windowWidth = windowWidthIn;
		windowHeight = windowHeightIn;
	}
}
